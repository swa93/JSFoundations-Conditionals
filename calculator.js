

const x = 5
const y = 3 
const operator = "-"

if (operator === "*") 
    console.log(`${x} * ${y} = ${x * y}`);


else if (operator === "/") 
    console.log(`${x} / ${y} = ${x / y}`);
  
 else if (operator === "+") 
    console.log(`${x} + ${y} = ${x + y}`);
  
else 
    console.log(`${x} - ${y} = ${x - y}`);

  